#include "list.h"
/**
 * add - add int
 * @a: input
 * @b: input
 * Return : sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub int
 * @a: input
 * @b: input
 * Return : diff
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul int
 * @a: input
 * @b: input
 * Return : mul
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div int
 * @a: input
 * @b: input
 * Return : div
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod int
 * @a: input
 * @b: input
 * Return : mod
 */

int mod(int a, int b)
{
	return (a % b);
}
